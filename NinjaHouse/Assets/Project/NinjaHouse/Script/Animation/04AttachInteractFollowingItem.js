// 各プレイヤーに追従するアイテムのID
const followingItemId = new ItemTemplateId("cdac28cc-d993-4926-93d1-37095ec5ccbf");

$.onInteract(player => {
    try {
        // 各プレイヤーに追従するアイテムを生成
        let followingItem = $.createItem(followingItemId, player.getPosition(), player.getRotation());

        // 生成したアイテムに追従対象プレイヤーを登録
        followingItem.send("RegisterPlayer", player);
    } catch (e) {
        // 例外処理
        $.log(e);
    }
})

